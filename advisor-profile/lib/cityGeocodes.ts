/** Alternate spellings / labels in C360 before dictionary lookup. */
const CITY_ALIASES: Record<string, string> = {
  manilla: 'manila',
}

function normalizeToken(token: string): string {
  return token.trim().toLowerCase().replace(/\s+/g, ' ')
}

/**
 * Resolves a CSV city label (e.g. "Makkah/mecca", "Riviera Maya") to [lat, lng].
 */
export function resolveCityCoords(city: string): [number, number] | null {
  const raw = city.trim()
  if (!raw) return null

  const parts = raw.split('/').map(normalizeToken)
  for (const part of parts) {
    const key = CITY_ALIASES[part] ?? part
    const hit = CITY_COORDINATES[key]
    if (hit) return hit
  }

  const whole = normalizeToken(raw)
  const key = CITY_ALIASES[whole] ?? whole
  return CITY_COORDINATES[key] ?? null
}
